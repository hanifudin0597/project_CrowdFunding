<?php

namespace App;

use Illuminate\Database\Eloquent\Model;

class otp_code extends Model
{
    protected static function boot() {
        parent::boot();
        static::creating(function ($model) {
            if ( ! $model->getKey()) {
                $model->{$model->getKeyName()} = (string) Str::uuid();
            }
        });
    }

    public function getIncrementing()
    {
        return false;
    }
}
